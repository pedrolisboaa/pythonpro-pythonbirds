"""
Módulo que contem uma operação de soma.
"""


def soma(numero1, numero2):
    """ Essa função calcula a soma de dois números
    :param numero1: numero
    :param numero2: numero
    :return: numero
    """
    return numero1 + numero2


if __name__ == '__main__':
    # Testando a função soma
    print(soma(5, 5))